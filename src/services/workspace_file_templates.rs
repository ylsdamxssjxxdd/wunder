fn build_zip_package(entries: &[(&str, &str)]) -> Result<Vec<u8>, String> {
    let cursor = std::io::Cursor::new(Vec::new());
    let mut zip = zip::ZipWriter::new(cursor);
    let options = FileOptions::default().compression_method(CompressionMethod::Deflated);
    for (name, content) in entries {
        zip.start_file(*name, options)
            .map_err(|err| format!("create template entry failed: {err}"))?;
        zip.write_all(content.as_bytes())
            .map_err(|err| format!("write template entry failed: {err}"))?;
    }
    let cursor = zip
        .finish()
        .map_err(|err| format!("finalize template archive failed: {err}"))?;
    Ok(cursor.into_inner())
}

#[cfg(test)]
mod tests {
    use super::build_workspace_file_template;

    #[test]
    fn builds_minimal_xlsx_template_for_empty_new_file() {
        let bytes = build_workspace_file_template("sheet.xlsx", "").expect("xlsx template");
        assert!(bytes.starts_with(b"PK"));
        assert!(bytes.len() > 100);
    }

    #[test]
    fn keeps_text_content_unmodified_for_non_empty_requests() {
        assert!(build_workspace_file_template("sheet.xlsx", "hello").is_none());
    }
}
