use std::io::Write;
use std::path::Path;

use zip::write::FileOptions;
use zip::CompressionMethod;

pub fn build_workspace_file_template(path: &str, requested_content: &str) -> Option<Vec<u8>> {
    if !requested_content.is_empty() {
        return None;
    }
    let extension = Path::new(path)
        .extension()
        .and_then(|value| value.to_str())
        .unwrap_or("")
        .trim()
        .to_ascii_lowercase();
    match extension.as_str() {
        "xlsx" => build_minimal_xlsx().ok(),
        "docx" => build_minimal_docx().ok(),
        "pptx" => build_minimal_pptx().ok(),
        _ => None,
    }
}

