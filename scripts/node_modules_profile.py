#!/usr/bin/env python
# -*- coding: utf-8 -*-
"""
Manage platform-specific node_modules directories under the repository root.

Profiles:
  - win-x86     -> node_modules-win-x86
  - linux-x86   -> node_modules-linux-x86
  - linux-arm   -> node_modules-linux-arm

The repository root keeps a single active entrypoint at ./node_modules.
On host machines this entrypoint should be a symlink/junction to the desired
profile directory. Docker compose mounts the Linux profile directory directly
to /workspace/node_modules, so container-side npm installs no longer rewrite
the host Windows dependency tree.
"""

from __future__ import annotations

import argparse
import os
import platform
import subprocess
import sys
from pathlib import Path


PROFILE_DIRS = {
    "win-x86": "node_modules-win-x86",
    "linux-x86": "node_modules-linux-x86",
    "linux-arm": "node_modules-linux-arm",
}

PROFILE_ALIASES = {
    "win": "win-x86",
    "windows": "win-x86",
    "windows-x86": "win-x86",
    "linux": "linux-x86",
    "linux-x64": "linux-x86",
    "linux-arm64": "linux-arm",
    "arm": "linux-arm",
    "arm64": "linux-arm",
}


def repo_root() -> Path:
    return Path(__file__).resolve().parents[1]


def active_node_modules_path() -> Path:
    return repo_root() / "node_modules"


def path_lexists(path: Path) -> bool:
    return os.path.lexists(path)


def resolve_profile_name(raw: str) -> str:
    value = str(raw or "").strip().lower()
    if not value:
        raise ValueError("profile is required")
    if value == "auto":
        system = platform.system().lower()
        machine = platform.machine().lower()
        if system == "windows":
            return "win-x86"
        if system == "linux":
            if machine in {"aarch64", "arm64"}:
                return "linux-arm"
            return "linux-x86"
        raise ValueError(f"unsupported host platform for auto profile: {platform.system()} {platform.machine()}")
    normalized = PROFILE_ALIASES.get(value, value)
    if normalized not in PROFILE_DIRS:
        options = ", ".join(["auto", *PROFILE_DIRS.keys()])
        raise ValueError(f"unknown profile: {raw}. expected one of: {options}")
    return normalized


def profile_path(profile: str) -> Path:
    return repo_root() / PROFILE_DIRS[profile]


def safe_resolve(path: Path) -> Path:
    return path.resolve(strict=False)


def redirect_target(path: Path) -> Path | None:
    if not path_lexists(path):
        return None
    absolute_path = path.absolute()
    resolved_path = safe_resolve(path)
    if resolved_path != absolute_path:
        return resolved_path
    return None


def is_known_profile_target(target: Path) -> bool:
    normalized_target = safe_resolve(target)
    return any(normalized_target == safe_resolve(profile_path(name)) for name in PROFILE_DIRS)


def remove_redirect(path: Path) -> None:
    if not path_lexists(path):
        return
    if os.name == "nt":
        result = subprocess.run(
            ["cmd", "/c", "rmdir", str(path)],
            capture_output=True,
            text=True,
            check=False,
        )
        if result.returncode != 0:
            raise RuntimeError(result.stderr.strip() or result.stdout.strip() or f"failed to remove redirect: {path}")
        return
    path.unlink()


def create_redirect(link_path: Path, target_path: Path) -> str:
    relative_target = os.path.relpath(target_path, link_path.parent)
    try:
        os.symlink(relative_target, link_path, target_is_directory=True)
        return "symlink"
    except OSError as symlink_error:
        if os.name != "nt":
            raise RuntimeError(f"failed to create symlink {link_path} -> {relative_target}: {symlink_error}") from symlink_error
        result = subprocess.run(
            ["cmd", "/c", "mklink", "/J", str(link_path), relative_target],
            capture_output=True,
            text=True,
            check=False,
        )
        if result.returncode != 0:
            message = result.stderr.strip() or result.stdout.strip() or str(symlink_error)
            raise RuntimeError(f"failed to create junction {link_path} -> {relative_target}: {message}") from symlink_error
        return "junction"


def status_lines() -> list[str]:
    root = repo_root()
    active = active_node_modules_path()
    lines = [f"repo_root: {root}", f"active_entry: {active}"]
    redirect = redirect_target(active)
    if not path_lexists(active):
        lines.append("active_state: missing")
    elif redirect is not None:
        lines.append(f"active_state: redirect -> {redirect}")
    elif active.is_dir():
        lines.append("active_state: directory")
    elif active.is_file():
        lines.append("active_state: file")
    else:
        lines.append("active_state: other")

    for name, directory in PROFILE_DIRS.items():
        target = root / directory
        marker = "active" if redirect is not None and safe_resolve(target) == redirect else "inactive"
        exists = "present" if target.exists() else "missing"
        lines.append(f"profile[{name}]: {target} ({exists}, {marker})")
    return lines


def command_status(_args: argparse.Namespace) -> int:
    for line in status_lines():
        print(line)
    return 0


def command_use(args: argparse.Namespace) -> int:
    profile = resolve_profile_name(args.profile)
    active = active_node_modules_path()
    target = profile_path(profile)
    target.mkdir(parents=True, exist_ok=True)

    current_redirect = redirect_target(active)
    if not path_lexists(active):
        link_kind = create_redirect(active, target)
        print(f"created {link_kind}: {active} -> {target.name}")
        return 0

    if current_redirect is not None:
        if safe_resolve(target) == current_redirect:
            print(f"already using profile {profile}: {target}")
            return 0
        if not is_known_profile_target(current_redirect):
            raise RuntimeError(
                f"active node_modules redirects to an unknown target: {current_redirect}. "
                "switch it manually before re-running this command."
            )
        remove_redirect(active)
        link_kind = create_redirect(active, target)
        print(f"switched {link_kind}: {active} -> {target.name}")
        return 0

    if active.is_dir():
        raise RuntimeError(
            f"{active} is a real directory. Use `adopt --profile {profile}` to move it into {target.name}, "
            "or rename it manually before switching."
        )
    raise RuntimeError(f"{active} exists but is not a directory redirect")


def command_adopt(args: argparse.Namespace) -> int:
    profile = resolve_profile_name(args.profile)
    active = active_node_modules_path()
    target = profile_path(profile)
    current_redirect = redirect_target(active)

    if current_redirect is not None:
        if safe_resolve(target) == current_redirect:
            print(f"active node_modules already redirects to {target.name}")
            return 0
        raise RuntimeError(
            f"{active} already redirects to {current_redirect}. Remove or switch it before adopt."
        )
    if not active.exists():
        raise RuntimeError(f"{active} does not exist; nothing to adopt")
    if not active.is_dir():
        raise RuntimeError(f"{active} is not a directory; cannot adopt")
    if target.exists():
        raise RuntimeError(f"{target} already exists; adopt would overwrite an existing profile directory")

    active.rename(target)
    link_kind = create_redirect(active, target)
    print(f"adopted existing node_modules into {target.name} and created {link_kind} at {active}")
    return 0


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Manage platform-specific node_modules profiles")
    subparsers = parser.add_subparsers(dest="command", required=True)

    status_parser = subparsers.add_parser("status", help="Show current node_modules profile state")
    status_parser.set_defaults(handler=command_status)

    use_parser = subparsers.add_parser("use", help="Switch active node_modules to a profile redirect")
    use_parser.add_argument("--profile", required=True, help="Profile name: auto/win-x86/linux-x86/linux-arm")
    use_parser.set_defaults(handler=command_use)

    adopt_parser = subparsers.add_parser(
        "adopt",
        help="Move an existing real ./node_modules directory into a named profile and replace it with a redirect",
    )
    adopt_parser.add_argument("--profile", required=True, help="Profile name: auto/win-x86/linux-x86/linux-arm")
    adopt_parser.set_defaults(handler=command_adopt)

    return parser


def main() -> int:
    parser = build_parser()
    args = parser.parse_args()
    try:
        return args.handler(args)
    except Exception as exc:  # noqa: BLE001
        print(f"error: {exc}", file=sys.stderr)
        return 1


if __name__ == "__main__":
    raise SystemExit(main())
