export const getLanguageLabel = (language) => {
  const code = String(language || "").trim();
  if (!code) {
    return "";
  }
  const key = `language.${code}`;
  const translated = t(key);
  if (translated && translated !== key) {
    return translated;
  }
  return languageLabels[code] || code;
};

